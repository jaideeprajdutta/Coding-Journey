#include<iostream>
using namespace std;
int main(){
    int x = 5;
    cout <<x<<endl;
    x = 10; // Updation of x
    cout <<x<<endl;
    // x = x + 5; // x = 15 
    x += 5;
    cout <<x<<endl;
    // x = x-10; // x = 5
    x -= 10;
    cout <<x<<endl;
    // x = x * 2; // x = 10
    x *= 2;
    cout <<x<<endl;
 }
